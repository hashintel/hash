/**
 * React hook that manages a Pyright WebWorker for Python language intelligence.
 *
 * Uses the pre-built Pyright worker from `monaco-pyright-lsp` and communicates
 * with it via standard LSP over postMessage. Exposes the same {@link LanguageClientApi}
 * interface as {@link useLanguageClient} so the provider can switch transparently.
 *
 * Init flow:
 * 1. Worker posts `{ type: "serverLoaded" }`
 * 2. We send `{ type: "initServer", userFiles, typeshedFallback }` with .pyi stubs
 * 3. Worker posts `{ type: "serverInitialized" }`
 * 4. We create an LSP message connection and send `initialize` + `initialized`
 * 5. We open all virtual Python files as LSP documents
 */
import {
  BrowserMessageReader,
  BrowserMessageWriter,
  createMessageConnection,
  type MessageConnection,
} from "vscode-languageserver/browser";
import {
  type CompletionList,
  type Diagnostic,
  type Hover,
  type SignatureHelp,
} from "vscode-languageserver-types";

import { useCallback, useEffect, useRef } from "react";

import type { SDCPN } from "../../core/types/sdcpn";
import type { VirtualFile } from "../lib/create-language-service-host";
import { filePathToUri, uriToFilePath } from "../lib/document-uris";
import { generatePythonVirtualFiles } from "../lib/generate-python-virtual-files";
import type {
  DocumentUri,
  Position,
  PublishDiagnosticsParams,
} from "./protocol";
import type { LanguageClientApi } from "./use-language-client";

// ---------------------------------------------------------------------------
// Pyright path helpers
// ---------------------------------------------------------------------------

/** User files root inside the Pyright ZenFS filesystem. */
const TYPINGS_ROOT = "/typings";

/**
 * Normalise an internal virtual-file path for Python import compatibility.
 * - `sdcpn-lib` → `sdcpn_lib` (Python can't import hyphens)
 * - Color-ID directories: strip non-identifier chars so the FS path matches
 *   the `from colors.<sanitised>.defs` import generated by the stub.
 */
function toPyrightFsPath(internalPath: string): string {
  return internalPath
    .split("/")
    .map((segment) => {
      if (segment.endsWith(".py") || segment.endsWith(".pyi")) {
        // File names: replace hyphens with underscores
        return segment.replace(/-/g, "_");
      }
      // Directories: strip characters invalid in Python identifiers
      return segment.replace(/-/g, "");
    })
    .join("/");
}

/**
 * Bidirectional mapping between Pyright URIs and internal paths.
 *
 * `toPyrightFsPath` is lossy (strips hyphens from UUIDs), so we can't
 * reconstruct internal paths from Pyright URIs by string manipulation alone.
 * Instead we maintain an explicit reverse map that is rebuilt whenever the
 * virtual file set changes.
 */
class PyrightUriMap {
  /** Internal path → Pyright URI */
  private forward = new Map<string, string>();
  /** Pyright URI → Internal path */
  private reverse = new Map<string, string>();

  rebuild(internalPaths: Iterable<string>): void {
    this.forward.clear();
    this.reverse.clear();
    for (const internalPath of internalPaths) {
      const pyrightUri = `file://${TYPINGS_ROOT}${toPyrightFsPath(internalPath)}`;
      this.forward.set(internalPath, pyrightUri);
      this.reverse.set(pyrightUri, internalPath);
    }
  }

  toPyrightUri(internalPath: string): string {
    return (
      this.forward.get(internalPath) ??
      `file://${TYPINGS_ROOT}${toPyrightFsPath(internalPath)}`
    );
  }

  fromPyrightUri(pyrightUri: string): string | null {
    return this.reverse.get(pyrightUri) ?? null;
  }

  pyrightUriToMonacoUri(pyrightUri: string): DocumentUri | null {
    const internalPath = this.fromPyrightUri(pyrightUri);
    if (!internalPath) {
      return null;
    }
    return filePathToUri(internalPath);
  }

  monacoUriToPyrightUri(monacoUri: DocumentUri): string | null {
    const internalPath = uriToFilePath(monacoUri);
    if (!internalPath) {
      return null;
    }
    return this.toPyrightUri(internalPath);
  }
}

// ---------------------------------------------------------------------------
// File-system builder (UserFolder for initServer)
// ---------------------------------------------------------------------------

interface UserFolder {
  [key: string]: string | UserFolder | ArrayBuffer;
}

function buildUserFolder(files: Map<string, VirtualFile>): UserFolder {
  const root: UserFolder = {};

  for (const [path, file] of files) {
    const fsPath = toPyrightFsPath(path);
    const parts = fsPath.replace(/^\//, "").split("/");
    let current: UserFolder = root;

    for (let i = 0; i < parts.length - 1; i++) {
      const part = parts[i]!;
      if (!(part in current) || typeof current[part] === "string") {
        current[part] = {};
      }
      current = current[part] as UserFolder;
    }

    const fileName = parts.at(-1)!;
    current[fileName] = (file.prefix ?? "") + file.content;
  }

  return root;
}

// ---------------------------------------------------------------------------
// Prefix offset helpers
// ---------------------------------------------------------------------------

function countNewlines(text: string): number {
  let n = 0;
  for (const ch of text) {
    if (ch === "\n") {
      n++;
    }
  }
  return n;
}

